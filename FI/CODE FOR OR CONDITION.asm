.MODEL SMALL
.STACK 100H
.DATA 
 STR DB "FOR OR CONDITION EXAMPLE////GIVE INPUT Y OR y:$"  
 SOO DB 0AH,0DH,"YOUR GIVEN INPUT VALID (MATCH THE OR CONDITION): $"
.CODE

MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    
    MOV AH,9
    LEA DX,STR
    INT 21H  
    
    MOV AH,1
    INT 21H
    MOV CL,AL 
    
    CMP AL,'Y'
    JE THEN
    CMP AL,'y'
    JE THEN 
    
    JMP ELSE
    
    THEN: 
    MOV AH,9
    LEA DX,SOO
    INT 21H
    MOV AH,2 
    MOV DL,CL   
    INT 21H
    JMP END_PROC 
    
    ELSE:
    MOV AH,4CH
    INT 21H
    
    END_PROC:
    MOV AH,4CH
    INT 21H
    
    MAIN ENDP
END MAIN