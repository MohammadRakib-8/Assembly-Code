MODEL SMALL
.STACK 100H
.DATA
  STR DB "FOR LOOP FOR PRINT 1 TO 5 :$"
.CODE

MAIN PROC 
    MOV DX,@DATA
    MOV DS,DX
    
    MOV CX,5
    MOV BH,'1'  
    
    MOV AH,9
    LEA DX,STR
    INT 21H
    
    MOV AH,2
    MOV DL,0AH
    INT 21H
    MOV DL,0DH
    INT 21H
        
FOR_LOOP:
CMP CX,0
JE END_PROC

MOV AH,2
MOV DL,BH
INT 21H

MOV AH,2
MOV DL,0AH
INT 21H
MOV DL,0DH
INT 21H

DEC CX
INC BH  

JMP FOR_LOOP

END_PROC:
MOV AH,4CH
INT 21H 

MAIN ENDP
END MAIN